package sequencial.processamento;

public class Exercicio05 {

    public static void main(String[] args) {

        // Exercício: Calcular o resultado da soma e multiplicação de dois valores.

        int valorA = 5;
        int valorB = 8;

        // Calculando a soma
        int soma = valorA + valorB;

        // Calculando a multiplicação
        int multiplicacao = valorA * valorB;

        System.out.println("A soma dos valores é: " + soma);
        System.out.println("A multiplicação dos valores é: " + multiplicacao);
    }
}
