package sequencial.processamento;

public class exercicio06 {
    public static void main(String[] args) {

        // Exercício: Calcular a média de três valores fixos.

        int primeiroValor = 70;
        int segundoValor = 85;
        int terceiroValor = 90;

        // Calculando a média dos três valores
        double media = (double) (primeiroValor + segundoValor + terceiroValor) / 3;

        System.out.println("A média dos três valores é = " + media);
    }
}
